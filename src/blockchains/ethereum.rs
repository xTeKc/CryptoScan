use ureq;

pub fn ethereum_req() -> Result<(), ureq::Error> {
  let req: String = ureq::get("https://api.coingecko.com/api/v3/ping")
  .set("Example-Header", "header value")
  .call()?
  .into_string()?;
  println!("{:?}", req);
Ok(())
}

pub fn call_ethereum_req() {
  let b = ethereum_req();
  println!("{:?}", b);
}